package task03;

public class Main {
    public static void main(String[] args) {
        //While executes while the condition is true
        int s = 0;
        while (s < 5) {
            s++;
            System.out.println(s);
        }
        //Do-while will always execute at least once
        int k = 10;
        do {
            k++;
            System.out.println(k);
        } while (k < 5);
    }
}
